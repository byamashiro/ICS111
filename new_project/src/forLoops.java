
public class forLoops {

	public static void main(String[] args) {
		for(int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		
		for (int i=2; i<20; i+=2) {
			System.out.println(i);
		}
		
		for (int i=1; i < 1000; i*=3) {
			System.out.println(i);
		}
		
		for (int i=17; i > 6; i--) {
			System.out.println(i);
		}
		
		

	}

}
